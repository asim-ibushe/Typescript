
export function Addition(no1:number,no2:number):number
{
    var ans:number=0;
    ans=no1+no2;
    return ans;
}

export function Substraction(no1:number,no2:number):number
{
    var ans:number=0;
    ans=no1-no2;
    return ans;
}

//concept about anonymous function
export var Multi=function(no1:number,no2:number)
{
    return no1*no2;
}